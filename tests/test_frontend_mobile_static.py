        self.assertNotIn("KlinePanel", stock_detail_source)
        self.assertNotIn("claimGateAlertType", stock_detail_source)
        self.assertIn("slice(-60)", price_line_source)
        self.assertIn("toLocaleTimeString", price_line_source)
        self.assertIn("--mobile-color-accent", price_line_source)
        self.assertIn("--mobile-color-positive", mobile_style_source)
        self.assertIn("--mobile-radius-panel", mobile_style_source)
