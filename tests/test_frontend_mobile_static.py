import unittest
from pathlib import Path


class FrontendMobileStaticTests(unittest.TestCase):
    def test_mobile_shell_uses_app_tabs_and_no_operations_wide_table(self) -> None:
        frontend_root = Path(__file__).resolve().parents[1] / "frontend" / "src"
        app_source = (frontend_root / "App.tsx").read_text(encoding="utf-8")
        main_source = (frontend_root / "main.tsx").read_text(encoding="utf-8")
        shell_source = (frontend_root / "components" / "mobile" / "MobileAppShell.tsx").read_text(encoding="utf-8")
        home_source = (frontend_root / "components" / "mobile" / "MobileHome.tsx").read_text(encoding="utf-8")
        stock_row_source = (frontend_root / "components" / "mobile" / "MobileStockRow.tsx").read_text(encoding="utf-8")
        operations_source = (frontend_root / "components" / "mobile" / "MobileOperations.tsx").read_text(encoding="utf-8")
        manual_order_source = (frontend_root / "components" / "mobile" / "MobileManualOrderModal.tsx").read_text(encoding="utf-8")
        settings_source = (frontend_root / "components" / "mobile" / "MobileSettings.tsx").read_text(encoding="utf-8")
        model_selection_source = (frontend_root / "components" / "mobile" / "modelSelection.ts").read_text(encoding="utf-8")
        stock_detail_source = (frontend_root / "components" / "mobile" / "MobileStockDetail.tsx").read_text(encoding="utf-8")
        price_line_source = (frontend_root / "components" / "mobile" / "MobilePriceLineChart.tsx").read_text(encoding="utf-8")
        mini_trend_source = (frontend_root / "components" / "mobile" / "MobileMiniTrendChart.tsx").read_text(encoding="utf-8")
        mobile_style_source = (frontend_root / "mobile.css").read_text(encoding="utf-8")
        mobile_style_source += "\n".join(
            path.read_text(encoding="utf-8")
            for path in sorted((frontend_root / "styles" / "mobile").glob("*.css"))
        )

        self.assertIn('import "./mobile.css";', main_source)
        self.assertIn("if (isMobile) {", app_source)
        self.assertIn("<MobileAppShell", app_source)
        self.assertIn('"首页"', shell_source)
        self.assertIn('"单票"', shell_source)
        self.assertIn('"复盘"', shell_source)
        self.assertIn('"试验"', shell_source)
        self.assertIn('"设置"', shell_source)
        self.assertNotIn('"自选"', shell_source)
        self.assertIn("ExperimentOutlined", shell_source)
        self.assertIn("<ShortpickLabView canTrigger={props.isRootUser}", shell_source)
        self.assertIn('tab === "shortpick"', app_source)
        self.assertIn('tab === "operations" && !props.operations && !props.simulation && !props.operationsLoading', shell_source)
        self.assertNotIn("TrackHoldingsTable", operations_source)
        self.assertNotIn("SimulationTrackCard", operations_source)
        self.assertNotIn("mobile-action-dock", operations_source)
        self.assertIn("查看历史", operations_source)
        self.assertIn("mobile-history-overlay", operations_source)
        self.assertIn("isMeaningfulHistoryEvent", operations_source)
        self.assertIn('actionSummary === "持有"', operations_source)
        self.assertIn("buildStockRecords", operations_source)
        self.assertIn("recent_orders", operations_source)
        self.assertIn('event.event_type !== "order_filled"', operations_source)
        self.assertIn("onOpenManualOrder", operations_source)
        self.assertIn("MobileManualOrderModal", app_source)
        self.assertIn("mobile-manual-order-dialog", manual_order_source)
        self.assertIn("外观主题", settings_source)
        self.assertIn("本机 Codex GPT", settings_source)
        self.assertIn('setPanel("model")', settings_source)
        self.assertIn("props.onSelectAnalysisModel(keyId)", settings_source)
        self.assertIn("Switch size=\"small\"", settings_source)
        self.assertIn("onClick ? <RightOutlined /> : null", settings_source)
        self.assertNotIn("规范路由", settings_source)
